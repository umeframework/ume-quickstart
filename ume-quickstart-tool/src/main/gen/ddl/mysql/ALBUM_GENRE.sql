
/* ALBUM_GENRE : 唱片风格分类描述表 */
DROP TABLE IF EXISTS `ALBUM_GENRE` ;
CREATE TABLE `ALBUM_GENRE` (
    `GENRE_ID` CHAR(2) BINARY NOT NULL	 COMMENT '风格编码'
    ,`GENRE_NAME` VARCHAR(32) BINARY NOT NULL	 COMMENT '风格名称'
    ,`COMMENT` VARCHAR(512) BINARY 	 COMMENT '风格描述'
    ,`CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者'
    ,`CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
    ,`UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者'
    ,`UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '唱片风格分类描述表' ;
ALTER TABLE `ALBUM_GENRE` ADD CONSTRAINT `PK_ALBUM_GENRE` PRIMARY KEY ( GENRE_ID ) ;

