
DROP TABLE IF EXISTS `ALBUM` ;
CREATE TABLE `ALBUM` (
    `ID` INT AUTO_INCREMENT PRIMARY KEY COMMENT '编号'
    ,`TITLE` VARCHAR(128) BINARY NOT NULL	 COMMENT '标题'
    ,`ARTIST` VARCHAR(32) BINARY NOT NULL	 COMMENT '艺术家'
    ,`ISSUE_DATE` DATE 	 COMMENT '发行时间'
    ,`GENRE` CHAR(2) BINARY 	 COMMENT '风格'
    ,`COVER_PHOTO` BLOB 	 COMMENT '封面设计'
    ,`CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者'
    ,`CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
    ,`UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者'
    ,`UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '唱片基本信息表' ;


DROP TABLE IF EXISTS `ALBUM_TRACK` ;
CREATE TABLE `ALBUM_TRACK` (
    `ALBUM_ID` INT NOT NULL	 COMMENT '所属唱片'
    ,`TRACK_NO` INT NOT NULL	 COMMENT '曲目编号'
    ,`TRACK_NAME` VARCHAR(128) BINARY NOT NULL	 COMMENT '曲目名称'
    ,`PLAY_TIME` DECIMAL(4,2) 	 COMMENT '播放时间'
    ,`CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者'
    ,`CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
    ,`UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者'
    ,`UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '唱片曲目信息表' ;
ALTER TABLE `ALBUM_TRACK` ADD CONSTRAINT `PK_ALBUM_TRACK` PRIMARY KEY ( ALBUM_ID ,TRACK_NO ) ;


DROP TABLE IF EXISTS `ALBUM_CONTRIBUTOR` ;
CREATE TABLE `ALBUM_CONTRIBUTOR` (
    `ALBUM_ID` INT NOT NULL	 COMMENT '唱片编号'
    ,`TRACK_NO` INT(128) NOT NULL	 COMMENT '参与曲目'
    ,`CONTRIBUTOR` VARCHAR(32) BINARY NOT NULL	 COMMENT '艺术家'
    ,`COMMENT` VARCHAR(256) BINARY 	 COMMENT '备注'
    ,`CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者'
    ,`CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
    ,`UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者'
    ,`UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '参加该唱片录音的艺术家信息管理表' ;
ALTER TABLE `ALBUM_CONTRIBUTOR` ADD CONSTRAINT `PK_ALBUM_CONTRIBUTOR` PRIMARY KEY ( ALBUM_ID ,TRACK_NO ,CONTRIBUTOR ) ;


DROP TABLE IF EXISTS `ALBUM_GENRE` ;
CREATE TABLE `ALBUM_GENRE` (
    `GENRE_ID` CHAR(2) BINARY NOT NULL	 COMMENT '风格编码'
    ,`GENRE_NAME` VARCHAR(32) BINARY NOT NULL	 COMMENT '风格名称'
    ,`COMMENT` VARCHAR(512) BINARY 	 COMMENT '风格描述'
    ,`CREATE_AUTHOR` VARCHAR(32) BINARY COMMENT '创建者'
    ,`CREATE_DATETIME` TIMESTAMP DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间'
    ,`UPDATE_AUTHOR` VARCHAR(32) BINARY COMMENT '更新者'
    ,`UPDATE_DATETIME` TIMESTAMP  DEFAULT CURRENT_TIMESTAMP COMMENT '更新时间'
) COMMENT '唱片风格分类描述表' ;
ALTER TABLE `ALBUM_GENRE` ADD CONSTRAINT `PK_ALBUM_GENRE` PRIMARY KEY ( GENRE_ID ) ;


